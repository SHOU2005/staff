// ─────────────────────────────────────────────────────────────────────────────
//  Switch Captain — Community Data Layer
//  All community data stays in localStorage. Keys never conflict with existing.
// ─────────────────────────────────────────────────────────────────────────────
import toast from 'react-hot-toast';
import { getCandidates, getCaptains, getSettings, type Candidate, type Captain } from './data';

// ─── STORAGE KEYS ────────────────────────────────────────────────────────────
export const COMM_KEYS = {
  communities:     'switch_communities',
  communityPosts:  'switch_community_posts',
  broadcasts:      'switch_broadcasts',
  polls:           'switch_polls',
  workerProfiles:  'switch_worker_profiles',
  rehireLog:       'switch_rehire_log',
};

// ─── TYPES ───────────────────────────────────────────────────────────────────
export interface Community {
  id:          string;
  captainId:   string;
  captainName: string;
  name:        string;
  coverColor:  string;
  createdAt:   string;
  memberIds:   string[];
  pinnedPostId: string | null;
  stats: {
    totalMembers:      number;
    activePlacements:  number;
    available:         number;
    rehires:           number;
  };
}

export interface CommunityPost {
  id:          string;
  communityId: string;
  captainId:   string;
  type:        'announcement' | 'job' | 'milestone' | 'auto_success';
  content:     string;
  reactions:   { thumbsUp: number; fire: number };
  postedAt:    string;
  pinned:      boolean;
  autoGenerated: boolean;
}

export interface Broadcast {
  id:            string;
  fromCaptainId: string;
  communityId:   string;
  jobTitle:      string;
  location:      string;
  salary:        string;
  message:       string;
  targetFilter:  { jobTypes: string[]; availableOnly: boolean };
  sentAt:        string;
  seenBy:        string[];
  respondedBy:   string[];
}

export interface Poll {
  id:          string;
  communityId: string;
  question:    string;
  options:     string[];
  responses:   Record<string, string>;
  createdAt:   string;
  expiresAt:   string;
  active:      boolean;
}

export interface WorkerProfile {
  id:            string;
  candidateId:   string;
  name:          string;
  mobile:        string;
  currentStage:  string;
  jobType:       string;
  location:      string;
  statusMessage: string;
  lastUpdated:   string;
}

export interface RehireLog {
  id:                   string;
  originalCandidateId:  string;
  originalPlacedAt:     string | null;
  rehireSubmittedAt:    string;
  rehireSubmittedBy:    string;
  newPipelineEntryId:   string;
  payoutAmount:         number;
  payoutStatus:         'pending' | 'confirmed' | 'paid';
  billedToOps:          boolean;
}

